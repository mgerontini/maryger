package sentimentanalysis.common;

/**
 *
 * @author j
 */
public class User {
    long id;
    int followersCount;
    int friendsCount;
    int statusesCount;

    public User(long id, int followersCount, int friendsCount, int statusesCount) {
        this.id = id;
        this.followersCount = followersCount;
        this.friendsCount = friendsCount;
        this.statusesCount = statusesCount;
    }
    
    public int getFollowersCount() {
        return followersCount;
    }

    public int getFriendsCount() {
        return friendsCount;
    }

    public long getId() {
        return id;
    }

    public int getStatusesCount() {
        return statusesCount;
    }

    @Override
    public String toString() {
        return "User{" + "id=" + id + ", followersCount=" + followersCount + ", friendsCount=" + friendsCount + ", statusesCount=" + statusesCount + '}';
    }
    
}
